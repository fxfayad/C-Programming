#include<stdio.h>
int main()
{
    int i, t,n;
    scanf("%d",&t);

    for(i = 0; i < t; i++)
    {
        scanf("%d",&n);
        if(n % 3 == 0)
        {
            printf("%d is a multiple of 3\n",n);
        }
        else
        {
         printf("%d is not a multiple of 3\n", n);
        }

    }
    return 0;
}
